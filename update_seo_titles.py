def update_frontmatter(filepath: str, slug: str, new_title: str, new_desc: str) -> bool:
    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()

    if not content.startswith("---"):
        print(f"  SKIP (no frontmatter): {slug}")
        return False

    # Replace title
    content_new = re.sub(
        r'^(title:\s*")[^"]*(")',
        lambda m: m.group(1) + new_title + m.group(2),
        content,
        count=1,
        flags=re.MULTILINE,
    )

    # Replace description
    content_new = re.sub(
        r'^(description:\s*")[^"]*(")',
        lambda m: m.group(1) + new_desc + m.group(2),
        content_new,
        count=1,
        flags=re.MULTILINE,
    )

    if content_new == content:
        print(f"  NO CHANGE: {slug}")
        return False

    with open(filepath, "w", encoding="utf-8", newline="\n") as f:
        f.write(content_new)

    return True


def main():
    updated = 0
    skipped = 0
    errors = 0

    for slug, (new_title, new_desc) in UPDATES.items():
        filepath = os.path.join(ARTICLES_DIR, f"{slug}.mdx")
        if not os.path.exists(filepath):
            print(f"  NOT FOUND: {slug}.mdx")
            errors += 1
            continue
        try:
            changed = update_frontmatter(filepath, slug, new_title, new_desc)
            if changed:
                print(f"  OK: {slug}")
                updated += 1
            else:
                skipped += 1
        except Exception as e:
            print(f"  ERROR [{slug}]: {e}")
            errors += 1

    print(f"\n=== Done: {updated} updated / {skipped} skipped / {errors} errors ===")


if __name__ == "__main__":
    main()
